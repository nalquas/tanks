	players[2]={alive=true,score=0,sprite=321,spriteChangeDir=0,spriteChange=0,x=224,y=67,direction=6,speed=0,t_dirChange=timeBetweenDirChanges/2,tShot=0,destructStep=0,tDestruct=0}
	
